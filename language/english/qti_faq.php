<a id="F1"></a><b>What is BBCode?</b><br />
BBCode is a special implementation of HTML. Whether you can use BBCode is determined by the administrator. BBCode itself is similar in style to HTML: tags are enclosed in square braces [ and ] rather than &lt; and &gt; and it offers greater control over what and how something is displayed.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="F2"></a><b>Can I use HTML?</b><br />
Yes. To ensure a better readability, it is recommended to enclose your html with the BBC tags [code] and [/code].

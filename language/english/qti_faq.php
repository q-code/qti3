<a id="F4"></a><b>Can I post Images?</b><br />
Images as well as other documents can indeed be attached in your posts. It's up to the Administrator to define which type of document and which document size are allowed. If allowed, you can display the attached image in your text by using the BBCode [img]@[/img].

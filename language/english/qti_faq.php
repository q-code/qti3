<a id="S1"></a><b>How do I change my profile?</b><br />
Your profile (if you are registered) is stored in the database. To alter them click the Profile link (or on your username in any list page). This will allow you to change all your information.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="S2"></a><b>How do I show an image with my username?</b><br />
There may be one image below a username when viewing posts. This image is sometime an avatar. It is up to the board administrator to enable user's picture. If you are unable to add your photo (or avatar) from the Profile page, it's because the administrator decide it, certainly for good reasons.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="S3"></a><b>How do I change my rank?</b><br />
You cannot change the wording of any rank (ranks appear next to your username in the memberlist and bellow your name in the message pages). This board use ranks to identify certain roles. For example, staff members and administrators have special roles. It's up to an administrator to promote a member to the staff rank. A staff member has the right to close/re-open a ticket, to move a ticket to the correct section, and to modify the content. An administrator can also perform these actions (and much more).
<br />

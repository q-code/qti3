<a id="U1"></a><b>What are Administrators?</b><br />
Administrators are people assigned the highest level of control over the entire board. These people can control all facets of board operation which include setting permissions, banning users, creating staff members, controlling the board security, style, etc. They also have full capabilities of the staff members, in all sections.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="U2"></a><b>What are Section coordinators?</b><br />
Section coordinators are individuals whose job it is to look after the running of the sections from day to day. Like the staff members, they have the power to edit or delete posts and close, re-open, move, and delete tickets in the section they are responsible for. Generally coordinators are there to help users, to prevent people going off-topic or posting abusive or offensive material.

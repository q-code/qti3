<a id="P2"></a><b>How do I edit or delete a post?</b><br />
Unless you are the board admin or staff members you can only edit or delete your own posts. You can edit a post by clicking the edit button for the relevant post. If someone has already replied to the post, you will find a small piece of text output below the post; it also will appear if staff members or administrators edit the post (they should leave a message saying what they altered and why). Please note that normal users cannot delete a post once someone has replied.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="P3"></a><b>How do I add a signature to my post?</b><br />
To add a signature to a post you must first create one; this is done via your profile. When a signature is created, it will be displayed with your message. To hide your signature, just change it to an empty text in your profile.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="P4"></a><b>Why can't I access the sections or the messages?</b><br />
The board administrator can decide to limit the access for unregistered users. In this case, only registered users will be granted to see the sections and the messages. In other cases, unregistered users can see the sections and the tickets list, but are not allowed to see the replies. It's the same for the search tool or the memberlist. It's up to the board administrator to decide which part of the board is accessible for visitors.
<br />
<a href="#top">Back to top</a><br /><br />

<a id="P5"></a><b>Which sections are accessible to me?</b><br />
Members can access two kinds of sections. The public sections and the private sections. A third king is visible only for the staff members and the administrators. In a private section, the user will only see his own tickets, while in a public section, all tickets and replies are visible by the board members. Note that a section can be closed. This means that it is still accessible, but further posts are no more possible.

<a id="L1"></a><b>Waarom moet ik registreren?</b><br />
Registratie geeft je toegang tot bijkomende functies die voor gasten niet beschikbaar zijn. Het registreren bestaat over het algemeen uit het invullen van je naam, e-mail en wachtwoord, de overige gegevens zijn optioneel. Het registreren neemt dus niet veel tijd in beslag.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="L2"></a><b>Hoe kan ik registreren?</b><br />
In de registratiepagina zult u het gebruiksbeleid vinden. Lees het zorgvuldig. Als u met de voorwaarden het akkoord gaat, kunt u uw gebruikersbenaming kiezen. Afhankelijk van de registratieveiligheid die door de Beheerder wordt bepaald zult u direct uw wachtwoord kiezen en het moeten bevestigen, OF uw e-mailadres  ingaan (een tijdelijk wachtwoord zal verzendt naar u zijn). U zult ook een code moeten kopi&euml;ren die in de vorm wordt getoond. Als de gebruikersbenaming u kiest al door een raadslid wordt gebruikt, zult u een andere gebruikersbenaming moeten kiezen.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="L3"></a><b>Kan ik mijn E-mail verbergen?</b><br />
In uw Profiel zult u een privacyoptie vinden om uw e-mail voor niet geregistreerde bezoekers of voor leden te verbergen.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="L4"></a><b>Ik ben mijn wachtwoord kwijt!</b><br />
Geen paniek! De wachtwoorden kunnen niet opgehaald worden, wel kan er een nieuw wachtwoord aangemaakt worden. Om dit te doen ga je naar de Inloggen pagina en daar klik je op <u>Wachtwoord vergeten</u>, volg hierna de instructies, er wordt een wachtwoord opgestuurd naar het email adres dat in je profiel staat.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="L5"></a><b>Ik was in het verleden geregistreerd maar kan niet meer inloggen!</b><br />
De meest aannemelijke redenen hiervoor zijn; je hebt een onjuiste gebruikersnaam of wachtwoord ingegeven (kijk de e-mail die je toen bij registratie hebt gekregen) of de beheerder heeft uw account gewist voor enkele bepaalde redenen. Je hebt misschien nooit iets gepost? Het is gebruikelijk om periodiekz gebruikers te wissen die niets hebben gepost om de ruimte van de database te beperken. Probeer opnieuw te registreren en doe mee aan de discussies.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="L6"></a><b>Ik ben geregistreerd maar kan niet inloggen!</b><br />
Hebt u geregistreerd? Ernstig, moet u registreren in te loggen. Bent u verboden van het systeem ? (Zal het a- bericht worden getoond als u hebt) Als zo, zou u de webmaster of administratoren moeten contacteren om waarom te weten te komen. Als u geregistreerd bent ( en niet verboden) en u nog niet inloggen kunt, controleer uw gebruikersbenaming en wachtwoord dubbel. Gewoonlijk is dit het probleem; als niet, contacteer de administratoren.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="S1"></a><b>Hoe verander ik mijn profiel?</b><br />
Uw profiel (als u wordt geregistreerd) wordt opgeslagen in het gegevensbestand. Om te veranderen klikken hen de verbinding van het Profiel (of op uw gebruikersbenaming in om het even welke lijstpagina). Dit zal u toestaan om al uw informatie te veranderen.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="S2"></a><b>Hoe toon ik een beeld met mijn gebruikersbenaming?</b><br />
Er kan &eacute;&eacute;n beeld (foto) zijn onder een gebruikersbenaming wanneer het bekijken van posten. Dit beeld kan ook een avatar zijn. Het is aan de administrator fotos toe te laten. Als u foto van de pagina van het Profiel niet kunt toevoegen, is het omdat de beheerder het, zeker om goede redenen beslist.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="S3"></a><b>Hoe verander ik mijn rang?</b><br />
U kunt de verwoording van om het even welke rang (de rangen lijken naast uw gebruikersbenaming in memberlist en de blaasbalg uw naam in de berichtpagina's) veranderen niet. Dit systeem gebruik rangschikt om bepaalde rollen te identificeren. Bijvoorbeeld, hebben de staffleden en de beheerders speciale rollen. Het is aan een beheerder een lid aan de staffleden rang te bevorderen. Een stafflid heeft het recht op dicht/een ticket heropenen, om een ticket te verplaatsen naar de correcte sectie, en de inhoud te wijzigen. Een beheerder kan deze acties (en veel meer) ook uitvoeren.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="S4"></a><b>Ik kan e-mail van een gebruiker klikken niet</b><br />
Droevig, maar gebruikers kan niet geregistreerde gebruikers (of gebeurtenisleden) verhinderen hun e-mail te zien. In deze situatie, zult u niet e-mail in de het profielpagina van de gebruiker noch aan de memberlijst pagina kunnen merken.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="P1"></a><b>Hoe post ik een ticket in een sectie ?</b><br />
Gemakkelijk. Klik de relevante knoop op de sectieschermen. U kunt moeten registreren alvorens u een bericht kunt posten.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="P2"></a><b>Hoe geef schrap ik of een post uit ?</b><br />
Tenzij u de van de systeem beheerder of stafflid bent kunt u uw eigen posten slechts uitgeven of schrappen. U kunt een post uitgeven door te klikken uitgeeft knoop voor de relevante post. Als iemand reeds heeft geantwoord op de post, zult u een klein stuk van tekstoutput onder de post vinden; het zal ook verschijnen als de staffleden of de beheerders de post uitgeven (zij zouden een bericht moeten verlaten zeggend wat zij veranderden en waarom). Gelieve te merken op dat de normale gebruikers een post niet kunnen schrappen zodra iemand heeft geantwoord.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="P3"></a><b>Hoe voeg ik een handtekening aan toe mijn post?</b><br />
Om een handtekening aan een post toe te voegen moet u eerst tot stand brengen; dit wordt gedaan via uw profiel. Wanneer een handtekening wordt gecre&euml;erd, zal het met uw bericht worden getoond. Om uw handtekening te verbergen, verander het enkel in een lege tekst in uw profiel.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="P4"></a><b>Waarom kan ik niet tot een sectie toegang hebben?</b><br />
De administrator kan beslissen de toegang voor niet geregistreerde gebruikers te beperken. In dit geval, slechts zullen de geregistreerde gebruikers worden verleend om de secties en de tickets te zien. In andere gevallen, kunnen de niet geregistreerde gebruikers de secties en de ticketlijst zien, maar mogen niet de antwoorden zien. Het is het zelfde voor het onderzoekshulpmiddel of memberlijst. Het is aan de administrator te beslissen welk deel van het systeem voor bezoekers toegankelijk is.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="P5"></a><b>Welke secties toegankelijk zijn voor me?</b><br />
De leden kunnen tot twee soorten secties toegang hebben. De openbare secties en de priv&eacute; sectie. Een derde koning is zichtbaar slechts voor de staffleden en de beheerders. In een priv&eacute; sectie, zal de gebruiker slechts zijn eigen tickets zien, terwijl in een openbaar sectie, alle antwoorden door de leden zichtbaar zijn. Merk op dat een forum kan worden gesloten. Dit betekent dat het nog toegankelijk is, maar de verdere posten zijn niet meer mogelijk.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F1"></a><b>Wat zijn BBCode?</b><br />
BBCode is een speciale vorm van HTML. Of wordt u BBCode kunt gebruiken bepaald door de beheerder. BBCode zelf is gelijkaardig in stijl aan HTML: de markeringen zijn ingesloten in vierkante steunen [ en ] eerder dan &lt; en &gt; en het biedt grotere controle aan over wat en hoe iets wordt getoond.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F2"></a><b>Kan ik HTML gebruiken?</b><br />
Ja. Om een betere leesbaarheid te verzekeren, wordt het geadviseerd om uw HTML met de BBC markeringen [code] en [/code] in te sluiten.<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F3"></a><b>Wat zijn Smileys?</b><br />
Smileys, of icons, is kleine grafische beelden die kunnen worden gebruikt om wat gevoel (of een toestand) uit te drukken. U kunt een smiley aan uw ticket (of uw antwoorden) van de het posten pagina toevoegen.<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F4"></a><b>Kan ik belden zenden?</b><br />
De beelden, maar ook andere type documenten, kunnen inderdaad in uw posten worden gebonden. De grote en type documenten zijn door de Administrator beslist. Wanneer dat toegestaan is, kunt u de beeld in uw bericht tonen met de BBCode [img]@[/img].
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F5"></a><b>Wat zijn Nieuws?</b><br />
Nieuws dat vaak het belangrijke informatie bevat zou en u hen zo spoedig mogelijk moeten lezen. Nieuws worden met een verschillend pictogram getoond en, over het algemeen, op de bovenkant van de tickets lijst waaraan zij worden gepost. Slechts kunnen de staffleden en de beheerders Nieuws verzenden.
<br />
<a href="#top">Terug naar boven</a><br /><br />

<a id="F6"></a><b>Wat zijn gesloten tickets?</b><br />
De gesloten tickets worden geplaatst deze manier omdat de actie rond is. U kunt op gesloten tickets antwoorden niet. De tickets kunnen om vele redenen worden gesloten.<br />
